package Array;

public class Globalvariable1 {
    static int id=101;
    static String name;
    static int age;

	public static void main(String[] args) {
    id=111;
    System.out.println("id is :"+id);
    System.out.println("name is :"+name);
    System.out.println("age is :"+age);
    System.out.println("_ _ _ _");
    id=102;
    name="shravani";
    age=22;
    System.out.println("id is :"+id);
    System.out.println("name is :"+name);
    System.out.println("age is :"+age);
	}

}
